// Deterministic weekly + monthly copy derived from Task 3 matchedRule (no LLM).
import type { InsightRuleId } from '@/lib/insightEngine';
import type { InsightRow } from '@/lib/supabaseInsights';

export type ExecutionPlan = {
  ruleId: InsightRuleId;
  bottleneckLabel: string;
  chainHint: string;
  weeklySteps: string[];
  monthlyDirection: string;
};

export function parseMatchedRule(insight: InsightRow | null): InsightRuleId | null {
  if (!insight?.data_context || typeof insight.data_context !== 'object') return null;
  const r = (insight.data_context as { matchedRule?: unknown }).matchedRule;
  if (r === 1 || r === 2 || r === 3 || r === 4 || r === 5) return r;
  return null;
}

export function buildExecutionPlan(insight: InsightRow | null): ExecutionPlan | null {
  const ruleId = parseMatchedRule(insight);
  if (ruleId === null) return null;

  switch (ruleId) {
    case 1:
      return {
        ruleId,
        bottleneckLabel: 'Продажи: лид → сделка',
        chainHint: 'Маркетинг даёт лиды, но сделки не закрываются — теряется середина воронки.',
        weeklySteps: [
          'Прослушать или разобрать записи 5 звонков с лидами за эту неделю.',
          'Зафиксировать шаг, на котором чаще всего «отваливается» сделка.',
          'Обновить короткий скрипт квалификации и следующий шаг после лида.',
          'Назначить ответственного за ответ лиду в течение 24 часов.',
          'Не увеличивать рекламный бюджет до итогов недели.',
        ],
        monthlyDirection:
          'Ожидаемый эффект: рост конверсии лид → сделка, меньше «дорогих» потерянных лидов, быстрее движение к оплате.',
      };
    case 2:
      return {
        ruleId,
        bottleneckLabel: 'Маркетинг: расход → лиды',
        chainHint: 'Деньги в канал уходят, а входящих заявок мало — проблема в верхней части воронки.',
        weeklySteps: [
          'Поставить расход на этом канале на паузу или снизить на 14 дней.',
          'Проверить лендинг, форму и первое сообщение после заявки.',
          'Сверить аудиторию и креатив с реальным ICP.',
          'Зафиксировать, на каком шаге лиды перестают отвечать.',
        ],
        monthlyDirection:
          'Ожидаемый эффект: либо восстановится стоимость лида, либо вы перенесёте бюджет в канал с доказанной отдачей.',
      };
    case 3:
      return {
        ruleId,
        bottleneckLabel: 'Деньги: выручка → кэш',
        chainHint: 'В отчёте есть выручка, но касса отрицательная — разрыв по срокам оплат.',
        weeklySteps: [
          'Список топ-5 должников: сумма, дата оплаты, ответственный.',
          'Контакт по каждому: новая дата платежа в календаре.',
          'Согласовать с финансами: отгрузки только после оплаты или по графику.',
          'Перенести следующий крупный расход после ожидаемого притока.',
        ],
        monthlyDirection:
          'Ожидаемый эффект: выравнивание притока с выручкой, снижение кассового разрыва и риска дефицита.',
      };
    case 4:
      return {
        ruleId,
        bottleneckLabel: 'Эффективность: рост расхода без роста результата',
        chainHint: 'Бюджет вырос, а выручка/конверсии не сдвинулись — средства сжигаются на той же воронке.',
        weeklySteps: [
          'Не запускать новые кампании 30 дней — зафиксировать текущие KPI.',
          'Найти один шаг воронки с самым большим отвалом.',
          'Сформулировать одну проверяемую гипотезу и тест на 7 дней.',
          'После теста — решение: масштабировать или откатить.',
        ],
        monthlyDirection:
          'Ожидаемый эффект: окупаемость стабилизируется — рост результата опережает рост расходов, меньше «пустого» бюджета.',
      };
    case 5:
      return {
        ruleId,
        bottleneckLabel: 'Без критического узкого места',
        chainHint: 'Метрики периода в допустимом коридоре — фокус на закреплении и аккуратном масштабировании.',
        weeklySteps: [
          'Выбрать один канал с лучшими цифрами по факту периода.',
          'Удвоить внимание к этому каналу на неделю (время команды, бюджет, контроль).',
          'Не открывать новые эксперименты до короткого review итогов.',
          'Записать 3 фактора, которые сработали — чтобы повторить.',
        ],
        monthlyDirection:
          'Ожидаемый эффект: сохранение устойчивости метрик и рост за счёт усиления уже работающего, а не хаотичных тестов.',
      };
  }
}
